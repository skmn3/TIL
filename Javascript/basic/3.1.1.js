const num = 5/2;

console.log(num); // 5와 2가 실수 취급된다.
console.log(Math.floor(num)); // 정수(내림)
console.log(Math.ceil(num)); // 정수(올림)
console.log(Math.round(num)); // 반올림
console.log(Math.trunc(num)); // 정수(내림)
console.log(Math.random()); // 0~1 사이의 난수
console.log(Math.random()*10); // 0~10 사이의 난수
console.log(Math.random()*10+1); // 1~11 사이의 난수
console.log(Math.random()*10+2); // 2~11 사이의 난수

# 탈출 문자

# \n 줄바꿈
print("가는 말이 고와야 \n오는 말이 곱다.")

# \"" \' : 문장 내에서 따옴표
# 저는 "무지개"입니다
print("저는 '무지개'입니다")
print('저는 "무지개"입니다')

print("저는 \"무지개\"입니다.")
print("저는 \'무지개\'입니다.")

# \\ : 문장 내에서  \
print("c:\\Users\\islms\\Desktop\\dsm\\2021\\Study-Python")

# \r : 커서를 맨앞으로 이동
print("4개소프트웨어마이스터고\r대덕")

# \b : 백스페이스 (한글자 삭제)
print("나는는\b바보")

# \t : 탭
print("뽀로로와\t친구들")

